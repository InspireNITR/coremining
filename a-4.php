        <h3 style="color:orange;">THE VICIOUS CIRCLE OF POVERTY AND HUNGER</h3>

        <p>Widespread and persistent hunger is a fundamental contradiction in today's world. The food is there: world
            agriculture produces 17 percent more calories per person today than it did 30 years ago, despite a 70
            percent population increase. Work in FAO shows that world agriculture can produce enough to feed humanity in
            the future without putting excessive pressure on prices or the environment. The existence of 780 million
            chronically hungry people in the developing world today shows that there is something fundamentally wrong in
            the distribution of food and the resources with which to access it.
            Hunger and poverty will remain at unacceptable levels unless purposeful action is taken to give them higher
            policy priority and to mobilize resources towards fighting them directly and towards agriculture and rural
            development. That is our principle message. Placing the MDGs at the center of Financing for Development is a
            step in the right direction.
        </p>

        <h3 style="color:orange;">Hunger and poverty: exploring the reverse linkages</h3>

        <p>Widespread hunger and malnutrition in a world of plentiful food implies that extreme poverty is the root
            cause of undernourishment. It is not always understood, however, that hunger and malnutrition (including
            micronutrient deficiencies) are in turn major causes of poverty. They affect the ability of individuals to
            escape poverty in several ways through-
        <ul>
            <li>Reducing the capacity for physical activity and hence the productive potential of the labour of those
                who suffer from hunger - and that is usually their only asset.</li>

            <li>Impairing people's ability to develop physically and mentally, retarding child growth, reducing
                cognitive ability and seriously inhibiting school attendance and performance - thus compromising the
                effectiveness
                of investment in education.</li>
            <li>Causing serious long-term damage to health, linked to higher rates of disease and premature death.
                Passing from generation to generation: Hungry mothers give birth to underweight children who start life
                with a handicap.</li>


            <li>Contributing to social and political instability that further undermines government capacity to reduce
                poverty.</li>
        </ul>
        </p>

        <h3 class="text-center" style="color:orange;">Hunger and Conflict</h3>

        <h4 style="color:orange;">Evidence on the Cost of Hunger</h4>

        <p>There is evidence that 46 million years of productive, disability-free life were lost in 1990, the result of
            lost social productivity caused by four types of malnutrition: stunting and disorders related to iodine,
            iron and vitamin A deficiency.</p>

        <ul>
            <li>156 million children under five in developing countries suffer from protein energy malnutrition. 90
                percent of all anaemic children and expectant mothers live in developing countries. Vitamin A deficiency
                is the primary cause of preventable eye defects and blindness in children.</li>
            <li>About 17 percent of infants suffer from intra-uterine growth retardation, an indication of poor maternal
                nutrition. Low birth weight means a high risk of illness and death during infancy and reduced work
                capacity and strength in adulthood.</li>
            <li>12 million deaths each year among children under five in the developing world - a staggering 55 percent
                - are associated with malnutrition.</li>
            <li>A study of Sierra Leonean agriculture showed that, on average, a 50 percent increase in calories per
                consumer equivalent would increase output by 16.5 percent, or 379 kg of food. The findings show that the
                lower the calorie intake is, the more significant output response is to increased calorie intake. For
                example, based on a daily intake of 1 500 calories per consumer equivalent, a mere 10 percent increase
                in calorie intake would increase output by nearly 5 percent.</li>
            <li>A study in Ethiopia has shown that a 10 percent improvement in weight-for-height and Body Mass Index
                (BMI) would lead to a 23 and 27 percent increase in output and wages respectively. In the same study
                height, an indicator of past nutritional experience, is found to be a significant determinant of wages,
                with a person who is 7.1 cm. above the average height, earning about 15 percent higher wages.</li>
            <li>An FAO study found that raising Dietary Energy Supply (DES) to 2 770 kcal/day in countries in which the
                average is below that level, would increase per capita GDP growth by 0.34 to 1.48 percentage points per
                year in countries where DES is below that level. Improved nutrition affects economic growth through
                greater labor productivity and life expectancy.</li>

            <li>3 percent of GDP every year is lost in some Asian countries through productivity losses resulting from
                stunting and iodine and iron deficiencies. Given that GDP growth rates in these countries were up to 7
                percent per annum in the nineties - it follows that large losses in GDP have taken place.</li>
            <li>50 percent of the economic growth in the United Kingdom and France between 1700 and 1900 resulted from
                improvements in nutrition and health, according to Nobel laureate Robert Fogel.</li>


        </ul>


        <p><b>Source: The State of Food and Agriculture, 2001 (FAO 200lb) which includes references to the studies,
                methodological details and caveats.</b></p>

        <p>During the last decade, food insecurity and malnutrition appear to have contributed to an increasing
            frequency of crisis events as well as to the vulnerability of countries to shocks. Most of today's armed
            conflicts and natural disasters are concentrated in regions heavily dependent on agriculture and in
            countries with a high proportion of food-insecure households and classified by FAO as "low-income food
            deficit".</p>

        <p>The impact of various crises will be also amplified when they affect a population that is already vulnerable
            and weakened by food insecurity. People in poor and food insecure countries are more likely to die from
            natural disaster than those who have developed better coping strategies to protect themselves. Crises often
            create the opportunity for underlying micronutrient deficiencies to develop into large outbreaks, for
            instance of pellagra or scurvy.</p>

        <p>The lack of sufficient resources for eradicating hunger will continue to put at risk the life of many
            vulnerable groups and will be one of the elements which contribute to the resurgence of emergencies.
            Therefore, savings from conflict avoidance should be understood as "returns" to aid. Following emergencies,
            humanitarian interventions are often necessary, but they are expensive and do not generally tackle the
            underlying causes of the crises. The need for relief will remain as long as vulnerable people do not get
            access to adequate food and to gainful economic opportunities. Timely investment in food , agriculture and
            rural development can help to break the repetitive cycle of hunger and conflict.</p>

        <p>The preceding discussion shows that alleviating hunger for those who suffer from it allows them to fully
            develop their physical and mental skills, increases their productivity and allows them to fully participate
            in the development process. Hunger reduction should therefore be thought as a productive investment in
            addition to a pressing moral obligation. Wisely conceived policies that target hunger directly and in a
            timely fashion, can break the "hunger trap".</p>



        <h3>The rural face of poverty and hunger and the role of agriculture</h3>


        <p>Agriculture is the principal driving force of the rural economy and, for those developing countries without
            substantial mineral resources,
            often the whole economy. Dependence on agriculture for economic growth and export earnings increases with
            the prevalence of hunger, and so
            does the proportion of people whose lives depend on the rural economy</p>



        <div class="col-md-8  p-4 mx-auto my-3">
            <img src="articles-img/4.3.jpg" class="img-fluid" alt="...">

        </div>





        <p>Hunger and poverty reduction require that the incomes of poor people and the sources from which they derive
            their livelihoods be enhanced. Therefore, pro-poor income growth needs to be encouraged. The question is:
            under what circumstances is income growth pro-poor? The short answer is that income growth originating in
            agricultural development will reduce poverty, provided that it does not occur in a context of high
            inequality in asset ownership.</p>



        <p>Rural households generate income from agriculture or employment in non-farm rural activities. Agricultural
            income originates from
            subsistence production, revenues from the sale of produce or employment in agriculture. The rural non-farm
            sector provides goods and services
            linked to agriculture, such as input preparation, repair of machinery and implements, output processing,
            transport and marketing. Income
            earned from agricultural activities creates demand for the output of small rural enterprises. It takes few
            skills to establish or work for
            such enterprises, so they are readily accessible to the poor. Initial productivity-induced growth in
            agricultural output will create
            multiplier effects in non-farm economies, increasing the incomes of those involved. It will also raise the
            incomes of those directly engaged
            in farming.</p>


        <h3 style="color:orange;">The importance of agricultural and rural growth for poverty alleviation</h3>

        <p>Recent literature explores the role of agriculture in poverty reduction. Some results are summarized here.
        </p>

        <ul>
            <li>Primary and tertiary sector growth reduce poverty, secondary sector growth has no significant impact on
                poverty.</li>
            <li>Growth in rural areas reduces poverty in both rural and urban areas, urban growth reduces poverty only
                in urban areas.</li>
            <li>Growth in small-farm production reduces the number of people in poverty and reduces its severity: the
                consumption of the poorest may be increased.</li>
            <li>In countries where income inequalities are small, labor productivity increases in agriculture are
                consistently more important than those other sectors in generating income increases .</li>
            <li>In sub-Saharan Africa, sustained growth in rural incomes, when widely distributed across households, is
                capable of unlocking significant additional growth.</li>
        </ul>



        <p>This process cannot work, however, if there are marked inequalities in access to agricultural and other
            assets, especially land. Large capital-intensive holdings tend to use inputs imported from abroad or from
            large towns and income is often invested outside the immediate area on which the farm is located;
            interaction with local economies will be limited. Small farms are much more fully integrated with local
            markets even if, ultimately, they are significant suppliers of export goods</p>






        <h3 style="color:orange;">TOWARDS A TWIN-TRACK STRATEGY</h3>

        <p>The above discussion shows that achieving massive and sustainable poverty reduction entails (a) targeting
            hunger directly so as to increase the productivity and productive potential of those who suffer from it, and
            allow them to take advantage of the opportunities offered by development and (b) stimulating agriculture and
            rural development, both essential for both overall economic growth and sustainable reductions in poverty.
            The productivity handicap caused by hunger must be dealt with directly if agricultural development is to
            proceed as it should. People in abject hunger must have enough to eat if they are to share the benefits of
            agricultural and rural growth. The vicious circle of undernourishment leading to low productivity and growth
            perpetuates underdevelopment and hunger. Hungry people must have better access to food, which requires
            direct assistance. The vicious circle must be broken. At the same time, concentration of poverty in the
            rural areas, implies that growth in agriculture and overall rural development are essential for a
            sustainable exit from poverty.</p>


        <p>Creating a synergy between direct action against hunger and measures to stimulate agriculture and the rural
            sector is fundamental to the twin-track strategy proposed here. The two tracks are complementary. Hungry
            people must have better access to adequate food as a precondition of their participation in development. At
            the same time, increases in agricultural productivity and production will increase rural economic activity
            and expand employment opportunities in the farm and rural non-farm sector. A case of "maximum synergy" is
            one in which safety nets and food assistance programs are supplied with local production: supplying safety
            nets with locally produced food whenever possible will lead to an expansion in market opportunities, farm
            output and employment, while providing food to those who need it.</p>


        <p>In general, agricultural development by small farmers in conjunction with policies to enhance the
            capabilities of the poor to access food, offers the best hope of a swift reduction in mass poverty and
            hunger. </p>

        <p>There is a lot of evidence that agriculture can contribute to poverty reduction beyond a direct effect on
            farmer's incomes. Agricultural development can stimulate economic development outside of the agricultural
            sector, and lead to higher job and growth creation. Increased productivity of agriculture raises farm
            incomes, increases food supply, reduces food prices, and provides greater employment opportunities in both
            rural and urban areas. Higher incomes can increase the consumer demand for goods and services produced by
            sectors other than agriculture. Such linkages (or the 'multiplier effect') between growth in the
            agricultural sector and the wider economy has enabled developing countries to diversify to other sectors
            where growth is higher and wages are better.</p>

        <h3 style="color:orange;">NEW VISION OF AGRICULTURAL EXTENSION</h3>

        <p>Agricultural extension reform requires policy vision and determination, and a nationwide strategy that can be
            implemented. Whether to decentralize and devolve, totally privatize or institute contractual arrangements
            with the private sector (including venture capital companies, non-governmental organizations, rural producer
            organizations), or promote end-user financing (or co-financing) of extension - these are country-specific
            questions requiring systematic analysis and preparation, gradual change, system coordination and system
            oversight.</p>

        <p>In putting together a strategy for extension and information services, it will be useful to refer to the
            diversified strategies and the emerging consensus on lessons learned mentioned in section I of this paper,
            to the proposals regarding the design and implementation23 of communication for rural development cited in
            section II, and also to the considerations in section III regarding food security.</p>

        <h3 style="color:orange;">NEW VISION OF RURAL DEVELOPMENT</h3>

        <p>A multi sectoral extension network offers an inclusive approach to rural development. It brings together
            agricultural extension providers, promotes communication for rural development, and establishes rural
            extension activities for non-agricultural populations in rural areas as well as an agricultural
            extension/communication strategy. In all cases where nationally integrated food security systems are being
            advanced, multiple sectors are encouraged to work collaboratively to combat food insecurity and generate
            income24</p>

        <div class="col-md-8  p-4 mx-auto my-3">
            <img src="articles-img/4.4.jpg" class="img-fluid" alt="...">
        </div>

        <p>An integrated food security network consists of a range of different sectors, including international
            organization projects, involving different agencies and organizations in the network. For example, in some
            countries along with the ministries of agriculture and science and technology, the ministries of
            transportation, public works, health and education may be involved. Each country will likely have a diverse
            set of national and transnational companies and third-sector organizations that may be part of a
            multisectoral network for public sector extension development.</p>

        <p>An insidious problem is how to reverse the top down attitude of extension agents and managers toward farmer
            groups in need of food security. Some of them hold the view that resource poor farmers are ignorant and
            incapable of managing production technologies and financial resources. These managers and extension agents
            think that as professionals they have the answers to all the farmers' problems. Experience and findings in
            the literature show neither perception to be true.</p>

        <h3 style="color:orange;">NEW VISION OF FOOD SECURITY</h3>

        <p>There is no single solution to what needs to be done to serve poor and food insecure populations. Some
            experts emphasize growth and greater production. Other experts fear overproduction and price slumps, and
            focus more on quality and marketing. Broad based and sustained growth will be essential to reduce poverty,
            according to the World Bank (2003a). If action is not taken to ensure proper rural livelihoods, the cities
            will be further swamped with unprepared poor people seeking a better way of life.</p>

        <div class="col-md-8  p-4 mx-auto my-3">
            <img src="articles-img/4.5.jpg" class="img-fluid" alt="...">
        </div>

        <p>Assisting rural populations to enhance agricultural productivity helps, but it is not sufficient. There is
            also the need to prepare farmers in non-farm related micro-enterprise development. Rural education and
            extension for health and nutrition require cooperation of various services. HIV/AIDS is a particularly
            troubling phenomenon . It should not be forgotten that in much of Africa but also elsewhere the HIV/AIDS
            epidemic and other diseases have affected large numbers of rural households. When they survive, the people
            in these households devote much of their time to just staying alive. What can be done that is not already
            being done?</p>

        <p>Clearly agricultural enterprise represents only one pathway to rural development. Thus, rural extension
            activities are needed in addition to agricultural extension to serve non-agricultural clientele. The State
            of Food and Agriculture 2000 (FAO 2002) asserts:</p>

        <b>"Reducing poverty and food insecurity involves enhancement of agricultural productivity and production, and
            income generation among producers as well as among those in rural areas who do not work the land.
            Institutions are the structuring features that command access of people to assets, to voice, and to power
            over their own lives, and that regulate competing claims to limited resources. Agricultural and rural
            extension (communication) programs are needed to reach out to those in rural areas who often enough
            constitute the majority population. It is fundamental for government to address those institutional,
            governance and politico-economic factors that tend to exclude individuals and population groups from
            progress. "</b>


        <h3 style="color:orange;">Recommendations and Conclusion:</h3>
        <ul>
            <li>It is recommended that governments develop a new and expanded policy agenda for agricultural extension
                and communication for rural development focusing national attention on food security and income
                generation of the rural poor.</li>
            <li>It is recommended that governments build a platform to promote dialogue and cooperation among relevant
                institutions and programs in all sectors with the aim of developing an extension and information
                services network for food security and income generation.</li>
            <li>It is recommended that governments activate institutional change within the public sector, aimed at
                supporting and promoting the new and expanded policy on extension and food security and the
                determinations instituted by the nationwide platform.</li>
        </ul>

