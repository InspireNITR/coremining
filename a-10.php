            <u> SAPLING PLANT WITH COIR BASED PACKET (Replacing present polythene
                based packet)</u>
        </h1>

        <br>


        <p>In recent years , plastic pollution has hit the headlines as a major environmental issue. While gardening is
            an outdoor activity involving organic material and natural products, the uncomfortable truth is that
            gardeners use a lot of plastic .Plastic plants pots are often used once –to bring on plants from seed or
            transport them home from the shops and then thrown away. While these pots are very convenient, such
            disposable items contribute significantly to plastic pollution as well as increase the environmental
            pollution. We can use biodegradable pots-made from coir in the place of plastic pots, concluding that bio
            –based pots were suitable alternatives to plastic. By replacing present polythene based packet with coir
            based packet ,it not only reduces the environmental pollution but also increases the mass scale employment,
            mostly in rural areas. In this context coir can make a significant contribution towards sustainable
            development .</p>





        <div class="col-md-6  p-4 mx-auto my-3">
            <img src="articles-img/10.1.jpg" class="img-fluid" alt="...">
        </div>



        <p>In the past, when coconuts were harvested for their delicious meat and juice ,the coconut husk was considered
            as a waste product. All of the material from the husk to the inner shell of the coconut was a discard
            product until people realized it had many applications in gardening and home products .People usually throw
            away tender coconut shells after drinking the water. We can replace the soil with coco –peat or husk.</p>


        <p>Coconut husk has high amount of lignin and cellulose that is why it has high calorific value.It can give a
            growth boost to the plant and help them absorb nutrient more effectively. Coir or coconut fibre is a natural
            fibre extracted from the outer husk of coconut and it is the fibrous material found between the hard
            ,internal shell and the outer coat of a coconut . The fibers from coconut husks .which are known as coir,
            are actually incredibly versatile and can be used in a variety of different products .Since ancient days
            ,the sturdiest coconut fibre have been used to fashion articles like baskets ,mats, ropes and bedding .</p>



        <p>It is found that present procedure of sapling plant in polythene based packet is hazardous as well as lower
            growth of plant .Coconut shells are fine growing medium because saplings can be transplanted directly into
            the soil along with the pot. Since they are biodegradable ,plants will not affected .In fact ,upon
            degradation these shells also provide nutrients to the saplings. So Coir based organic packet may be the
            best alternative considering huge advantage .</p>


        <div class="col-md-6  p-4 mx-auto my-3">
            <img src="articles-img/10.2.jpg" class="img-fluid" alt="...">
        </div>


        <p>Currently ,the global annual production of core fibre is about 350,000metric tons(MT). Yet ,even in the
            world’s top two producers ,India and Sri Lanka ,which account for about 90% of global coir fibre production
            ,combined, this renewable resource is underutilized ;local coir mills process only a fraction of the
            available husks ,which accrue more or less year round as a waste during coconut processing .India accounts
            for more than two-third of the world production of coir and coir products .The government has also played a
            vital role in reviving the lost frame of the traditional Indian industry by introducing various schemes and
            programmes to promote it .Introduction of schemes like SFURTI(Scheme of Fund for regeneration of Traditional
            industries),coir Udyami Yojana and Export market Promotion scheme has given wings to this golden industry.
            The production of coir has also extended to other coastal regions of Odisha, Goa, Andhra Pradesh .</p>


        <p>Coconut husk is the basic raw material for coir products. Around 50 percent of the available coir husk is
            used to produce coir products .Hence there is scope for growth of coir industry . The challenge for industry
            is to sustainable expand markets for this versatile renewable resource while maintaining its role as
            employer for the rural poor. This may require producers to innovative production improve product
            consistency. Emphasis has been given on developing devices /equipment machinery through R & D to reduce
            drudgery and to improve productivity of coir workers .</p>

        <div class="col-md-6  p-4 mx-auto my-3">
