        <p>Being a geo-textile fibre and bio-degradable ,Coir is extremely eco-friendly. Certain protective policy initiatives are required from the Govt. by way of fiscal incentives to popularize the use of these products. The coir ply should be exempted from the levy of excise duty and sales tax at least for ten years on the ground that it saves trees, helps prevent soil erosion ,reduces use of plastic packet so that it also reduces environmental pollution. While the world is looking for answers to problems of sustainable development coir industry is offering a win-win solution . What is now needed is a change in our mindset in favour of accepting an alternative strategy for sustainable development-Ecodevelopment.</p>











