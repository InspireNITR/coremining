        <p>melanoma indicate that the incidence of melanoma is increasing in those countries having
            high ratio of cases. As UV radiations can penetrate more easily in thin skin so there is greater
            number of incidence is found in thin skinned people. It is found that the incidence of
            Melanoma is more in children than adults. The chance of incidence of melanoma is correlated
            with UV exposure furthermore the survival chance of melanoma is less in boys as compared to
            girls . As the intensity of radiation increases in summer so the risk of melanoma in thin skinned
            people is increased in summer and it is more in females as compared to males as their skin is
            thinner than males . There is considerable relationship between melanoma risk and
            intermittent sun exposure and sunburn history. There is also a direct relationship between air
            travelling and melanoma incidence . However the studies revealed that genetic factors
            contribute more for having melanoma disease than behavioral aspects. The epidemiological
            studies of non melanoma skin carcinoma (NMSC) indicates that its risk is more in young
            females in lower limbs and sunbathing increases its risk five times in trunk region.</p>

        <h4 style="color:orange;">(C) Effects on Human Immunity:-</h4>

        <p>Exposure to UV radiations can also result in suppression of immune response to skin cancer,
            infectious diseases and other antigens. The immunosupression is due to changes in skin
            photoreceptors and antigen presenting cells that are brought by UV radiations. More increase
            in depletion of ozone results in more decrease in immune system.
        </p>




        <h4 style="color:orange;">(D) DNA Damage and Lung Diseases:-</h4>



        <p>Short exposure to UV-B radiations can cause the DNA damage because UV radiations can
            disturb biomolecules such as lipids, proteins and Nucliec acids. Due to UV-B radiations there
            would be cryptic transposable elements which may lead towards the mutations which is more
            dangerous than the immediate DNA damage. Excessive UV-B radiation exposure results in the
            basal and squamous cells carcinomas. These types of cancers are induced due to
            transcriptional errors during DNA replication which are caused by changes in pyrimidine bases.
            The ultimate cause of this whole mechanism is found to be the prolonged exposure to UV
            radiations. It is estimated that there is increase of 2% of incidence of these cancers by 1%
            depletion of ozone layer. Exposure to UV radiations equally affects lungs. Bronchitis,
            obstruction of lungs, Emphysema, asthma all can be resulted from UV radiations exposure.</p>


        <h4 style="color:orange;">(E) Effects of Hydrogen Peroxide on Human Health:-</h4>

        <p>Due to stratospheric ozone layer depletion UV radiations are penetrating in earth atmosphere
            which result in the production of reduced oxygen. Highly reactive species like hydrogen
            peroxide is produced which has bad effects on human health. It is ideal photochemical maker
            due to its long life and stability . Hydrogen peroxide is toxicant and it pollutes drinking water
            especially in lakes and makes water toxic and unfit for drinking. IT alters redox chemistry of
            metals that are used by our body like iron copper and manganese.</p>





        <h4 style="color:orange;">(F) Effect of Food Shortage on Human Population:-</h4>

        <p>Depletion of ozone layer is also causing the problem of food shortage to humans. UV
            radiations are disturbing developmental and physiological processes which is decreasing the
            productivity of crops. As humans are heavily dependent on crops for food so there is a great
            chance if depletion of ozone layer is not checked it may cause seriously shortage of food to
            humans. Researches also show that UV radiations can also be used to enhance yield of crops
            by the use and application of phytohormones.</p>


        <h3 style="color:orange;">5. Conclusion and Recommendations:-</h3>

        <p>Ozone layer is continuously depleting which is highly alarming situation of today.
            Chloroflourocarbons are major cause of ozone depletion. These substances should be banned
            or we should use their alternatives so that in future we can protect ourselves from the
            harmful effects of UV radiation. Human eye and skin are the most exposed part of the body to
            these radiations. So there is high degree of incidence of blindness and skin cancer disease
            increasing day by day with the depletion of ozone layer so we should use sunglasses and full
            body clothes especially in summer when there is high intensity of sunlight so that we can
            protect our body from harmful UV radiations. We should also use sun block creams to our
            most exposed parts of body like face. We should also don’t consume water from lakes as it
            may contain high quantity of hydrogen peroxide which is toxic to our bodies, and we should
            consume water for drinking from clean water sources.</p>




