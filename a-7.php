        <h3 style="color:orange;">INTRODUCTION:-</h3>

        <p>Many of human activities are badly affecting the atmosphere. Ozone depletion is one
            of those examples. Life is protected from UV rays by stratospheric ozone layer which
            acts as a shield or sun screen. Approximately 90% ozone is present in stratosphere at
            the height of 10 to 17 kilometer and is called ozone layer while remaining 10% is present
            in troposphere. Medium frequency UV radiations (200 nm to 315 nm) are absorbed 97
            to 99% by the ozone layer. Ozone (O3) is colourless just like oxygen (O2) but it has very
            harsh odour. It is very rare as compared to oxygen. It is estimated that out of 10 million
            air molecules about 2 millions are of O2 andonly 3 are of ozone. The process of ozone
            formation is called as photolysis. When the UV radiations from sun strike the O2
            molecules, it causes splitting of O2. Oxygen molecules react with oxygen atoms in the
            upper atmosphere to form ozone. Stratospheric ozone is measured from the ground in
            units called “Dobson Unit” (D.U). Normal ozone concentration is between 300 - 350 D.U.
            In our present study, we shall discuss how ozone layer depletion is affecting human
            health and we shall also discuss some recommendations to minimize ozone depletion
            and its adverse effects on human health.</p>


        <h3 style="color:orange;">Ozone Layer :-</h3>

        <p>The ozone layer is basically naturally occurring gas in the region of stratosphere where
            ozone particles are accumulated. Ozone layer is also naturally broken down but there is
            a balance between its formation and natural depletion. As a result the total amount of
            ozone remains constant. But ozone layer thickness varies with altitude and seasonal
            change. Ozone concentration is highest between 19 - 23 km. Most of ozone is formed
            at equator where there is maximum sunshine but with winds it travels at high altitude
            and get accumulated in stratosphere
        </p>

