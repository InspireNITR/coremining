        <h3>Causes of Ozone Depletion:-</h3>


        <h4 style="color:orange;">(A) Chlorofluorocarbons:-</h4>

        <p>Ozone depletion occurs when the natural balance between the production and
            destruction of stratospheric ozone is disturbed. Although natural phenomenon can
            cause ozone depletion but human activities such as CFCs are now accepted as major
            cause of depletion. All ozone depleting chemicals contain chlorine and bromine. CFCs
            are highly volatile and non combustible so they are very quickly evaporated and can
            easily reach in stratosphere where ozone is present here they start depleting ozone
            molecules. These CFCs have also adverse affects on human health. According to the
            chemical model for ozone destruction proposed about 20 years ago, the photolysis of
            Cl2O2 is key to ozone depletion reaction. But now atmospheric researchers studied that
            the rate of this reaction is not extremely high as it was thought previously so we can no
            longer say that CFCs are the main cause of ozone depletion.</p>

        <div class="col-md-8  p-4 mx-auto my-3">
            <img src="articles-img/7.3.jpg" class="img-fluid" alt="...">
        </div>

        <h4 style="color:orange;">(B) Unregulated Launches of Rockets:-</h4>

        <p>Another major cause of large scale ozone depletion is Rocket launches. It has been
            studied that unregulated rocket launches can result in much more ozone depletion than
            CFCs. It is estimated that if rocket launches will be let unregulated then it would cause
            huge ozone loss by the year 2050 than the CFCs have done.</p>



