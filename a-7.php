        <p>Exposure to UV radiations can cause skin cancer. UV radiations alter the structure of
            biomolecules and thus lead to different diseases Skin is the most often exposed part of body
            to UV radiations There are two types of skin cancer, Melanoma and Non-melanoma.
            Melanoma is most serious form of cancer and is often fatal, while non-melanoma is most
            common type and less fatal. Depletion of ozone layer leads to both Sun burn and skin cancer.
            UV radiations are also responsible for breast cancer and leukemia . Epidemiological studies of</p>


