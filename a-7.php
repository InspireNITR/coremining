        <h4 style="color:orange;">(D)Nitrogenous Compound:-</h4>


        <p>Nitrogenous Compounds emitted by human activities in small amount like NO, N2O and NO2
            are considered to be greatly responsible for the ozone layer.</p>


        <h3 style="color:orange;">Effects of Ozone Depletion:-</h3>

        <p>Ozone depletion is affecting the human health and environment negatively, as it allows the
            penetration of UV radiations to reach the Earth. These radiations can cause severe diseases in
            humans such as skin cancer, eye damage and genetic mutations etc. Furthermore the ozone
            depletion is affecting the aquatic life, biogeochemical cycles, air quality and also contributing
            in Global warming but in this review paper our main focus is on the effects of ozone depletion
            on human health.</p>


        <h4 style="color:orange;">(A) Effects on Eyes:-</h4>

        <p>The major cause of blindness in this world is cataracts. There would be 0.3% - 0.6% increase in
            risk of cataract if there will be 1% decrease in Ozone level . Eye lens can be damaged by
            oxidative agents. Oxidative oxygen produced by UV radiation can severely damage eye lens
            and cornea of eye is also badly damaged by UV radiation. Photokeratitis, cataract, blindness all
            are caused due to UV rays.</p>

