        <h3 style="color:orange;">Ozone Hole</h3>

        <p>Ozone hole is created in the region where ozone layer has been depleted. The term
            “Ozone hole” is applied when the depletion level is below 200 Dobson Unit (D.U). Ozone
            holes are first discovered in Antarctica in 1970. Few years ago ozone holes are also
            discovered in arctic region. Since 2000 rate of ozone depletion is increasing 0.5 percent
            per year. Due to depletion of Ozone UV rays are penetrating in troposphere and leading
            to more ozone formation in troposphere which is causing injurious effects on our health
            as ozone is toxic for our body.</p>




