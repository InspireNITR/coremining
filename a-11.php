        <p>In the early 1990s, the Big-Bang theory for the Origin of the Universe had already been widely accepted by
            the science community and it was clear that the Universe is Expanding rather than being stagnant. Still, the
            one certain question unanswered was “How?”. The hot topic for discussion back then was that the Universe
            might
            have enough energy density to stop its expansion or so little energy density that it would never stop
            expanding;
            and to be honest, we are yet to have a full proof answer to that. According to all the observable data
            available
            at that time, the Universe was believed to be full of matter and Gravity is what pulls all matter together.
            Thus,
            theoretically, the Universe had to slow down, but the slowing had not been observed. In 1998, a major
            breakthrough
            to this aspect was achieved after the Hubble Space Telescope (HST) observations of very distant Supernovae
            concluded
            that the universe has not been slowing due to gravity but it was accelerating. At that time nobody even
            expected this neither knew how to explain it.</p>








        <div class="row">

            <div class="col-md-6  p-4 mx-auto my-3">
