        <p>First of all, we should not be confused with Dark Energy and Dark Matter. Those two terms are totally
            incompatible</p>

        <ul>
            <li>Dark energy: It is a complete mystery. The universe consists of dark energy which is approximately 68%
                while dark
                matter is about 27% and normal matter is about less than 5% of the universe.
                One of the explanations for dark energy was that it is a property of space. It is one of the
                unsolved mysteries of cosmology.</li>

            <li>Dark matter: It is a form of matter i.e. composed of particles which does not interact with
                electromagnetic field
                and hence it is called dark. It can’t be seen directly
                and it do not absorb, reflect or emit light, so by observing Electromagnetic Radiation, Dark Matter
                cannot be detected.</li>

            <li>Ordinary matter: Basically, Ordinary/Normal Matter is the stuff that we are made of. On a scientific
                note, on the
                scale of Elementary Particles, ordinary matter is defined as everything that is composed of Quarks and
                Leptons. The
                small-scale electrons, protons, neutrons etc.,
                the mid-scale plants, animals etc. as well as the large-scale planets, stars etc. are all a part of
                Normal Matter.</li>





        </ul>


        <h3>History and different proposed hypothesis:</h3>

        <p>In the late 1990s, astronomers conclude that the expansion of the universe was not slowing down due to
            gravity
            as predicted earlier. Instead, the expansion speed
            was increasing and hence there must be something which favours this accelerating universe and was called
            “Dark Energy”.</p>


        <p>Hubble played an important role in verifying, characterizing and constraining Dark Energy. The discovery of
            Supernova
            1997ff, located about 10 billion light-years away provided instrumental evidences for dark energy.
            About several billion years ago, dark energy became dominant and the expansion accelerated. This
            accelerating period
            was measured from the ground-based studies while Hubble’s observation of 1997ff gives the decelerating part
            of the
            expansion. This change from a decelerating universe to an accelerating universe showed that dark energy
            exists.
            An explanation for dark energy gives us an idea that it will be a new kind of dynamic energy fluid or field.
            It was
            predicted that there exists something which fills the space but at the same time its expansion of the
            universe is the
            opposite to that of matter and normal energy.
            A last possibility suggested was that Einstein’s theory of gravity is not completely correct. This will not
            only affect
            the expansion of the universe but it would also affect the way that the normal matter in galaxies behaves.
            So, it was
            suggested that the solution to dark energy can be a major modification to the existing gravity theory but it
            will be a challenge to the existing universe at the same time. How it will describe the motion of the bodies
            correctly as Einstein’s theory is known to do? Hence, a lot of drawbacks arise and the mystery of this dark
            energy continues.</p>


