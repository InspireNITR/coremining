        <p>By studying many supernovae at different distances, theorists came up with some sorts of explanations.
            The most eye-catching speculations among them was that
            either Einstein’s theory of gravity must be somehow wrong to a very significant extent, or there may exist
            some strange kind of Energy Fluid which is responsible for the observed Gravitational Anomalies. Hence,
            efforts towards developing a new theory were made that could include some kind of field that creates
            this Cosmic Acceleration. At that time, the scientists didn’t know what the correct explanation is,
            but they gave the solution a name and that’s what they named it as Dark Energy.</p>


        <h4>
            <center>[The term ‘Dark Energy’ was coined by Michel Turner in 1998]</center>
        </h4>



        <h3>Components of the Universe:</h3>



        <div class="row">

            <div class="col-md-6  p-4 mx-auto my-3">
